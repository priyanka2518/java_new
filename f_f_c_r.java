//design a method to print first ten characters(return method)
class  f_f_c_r
{
	public static void ffc(char ch1,char ch2)
	{
		for(char m=ch1;m<=ch2;m++)
		{
		  System.out.println(m);
		
		}
	}

	public static void main(String[] args) 
	{
		System.out.println("First 10 characters are");
		ffc('a','j');
		System.out.println();
	}
}
